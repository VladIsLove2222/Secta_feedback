import re


def extract_first_name(full_name: str) -> str:
    """
    Извлекает имя клиента для обращения в опросе.

    Обрезает строку по первому из разделителей: пробел, скобка `(` или `[`,
    запятая. Это нужно потому, что в yclients имя клиента часто содержит
    фамилию или служебные пометки в скобках (предпочтения по стрижке,
    теги VIP и т.п.).

    Примеры:
        "Алексей Корешков"           -> "Алексей"
        "Иван (бокс-фейд каждые 2н)" -> "Иван"
        "Михаил [VIP]"               -> "Михаил"
        "Пётр, постоянник"           -> "Пётр"
        "Анна-Мария"                 -> "Анна-Мария"
        "  Сергей  "                 -> "Сергей"
        ""                           -> ""
        "(заметка без имени)"        -> ""
    """
    if not full_name:
        return ""

    name = re.split(r"[\s(\[,]", full_name.strip(), maxsplit=1)[0]
    return name.strip()
