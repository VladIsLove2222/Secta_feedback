import pytest

from app.names import extract_first_name


@pytest.mark.parametrize(
    "input_name,expected",
    [
        # Базовые случаи
        ("Алексей Корешков", "Алексей"),
        ("Иван", "Иван"),
        ("X", "X"),
        # Скобки с пометками
        ("Иван (бокс-фейд)", "Иван"),
        ("Михаил (любит покороче)", "Михаил"),
        ("Сергей (постоянник, скидка 10%)", "Сергей"),
        ("Дмитрий [VIP]", "Дмитрий"),
        # Запятые
        ("Пётр, постоянник", "Пётр"),
        ("Андрей, тех. директор", "Андрей"),
        # Составные имена через дефис не должны резаться
        ("Анна-Мария", "Анна-Мария"),
        ("Жан-Поль Бельмондо", "Жан-Поль"),
        # Лишние пробелы
        ("  Сергей  ", "Сергей"),
        ("  Алексей Корешков  ", "Алексей"),
        # Граничные случаи
        ("", ""),
        ("(заметка без имени)", ""),
        ("[только тег]", ""),
        (" ", ""),
    ],
)
def test_extract_first_name(input_name, expected):
    assert extract_first_name(input_name) == expected


def test_extract_first_name_handles_none_safely():
    """Защита от None — частый кейс при отсутствии поля в API."""
    assert extract_first_name(None) == ""  # noqa: E501
